package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Students\nToday we have Java class");

        System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch 1");

        System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");

        System.out.println("\\");

        System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");

        System.out.println("My Favorite TV show is \"Forensic Files\"");
    }
}

/*
1. \n = new line
2. \t = tab
3. \\ = backslash
4. \" = double quote
 */