package day04_concatenation;

public class Concatenation_vs_Addition {

    public static void main(String[] args) {

        System.out.println("32" +1); // result is 321
        System.out.println(1 + "7"); // 17

        System.out.println("---------------------------------------");

        System.out.println(12+2.5); //14.5
    }
}
